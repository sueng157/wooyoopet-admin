-- 실행 방법: Supabase SQL Editor에 전체 복사하여 실행
-- 목적: 견종/묘종 마스터 데이터 (앱에서 품종 검색 시 사용)
-- 참조: get_animal_kind.php → pet_breeds SELECT WHERE name ILIKE ?
-- MariaDB 원본: animalKind (dog) + animalKindX (cat) → type 컬럼으로 통합
-- 의존: 없음
-- ============================================================


-- ============================================================
-- 1. 테이블 생성
-- ============================================================

CREATE TABLE public.pet_breeds (
  id          bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  type        text NOT NULL DEFAULT 'dog',
  name        text NOT NULL,
  created_at  timestamptz DEFAULT now()
);

COMMENT ON TABLE public.pet_breeds IS '견종/묘종 마스터 — 품종 검색용';
COMMENT ON COLUMN public.pet_breeds.type IS 'dog 또는 cat (현재 dog만 운영)';
COMMENT ON COLUMN public.pet_breeds.name IS '품종명 (한글)';


-- ============================================================
-- 2. 제약조건
-- ============================================================

-- type은 dog 또는 cat만 허용
ALTER TABLE public.pet_breeds
  ADD CONSTRAINT pet_breeds_type_check CHECK (type IN ('dog', 'cat'));

-- 같은 분류 내 품종명 중복 방지
ALTER TABLE public.pet_breeds
  ADD CONSTRAINT pet_breeds_type_name_unique UNIQUE (type, name);


-- ============================================================
-- 3. 인덱스
-- ============================================================

-- 품종 검색 (ILIKE 패턴)
CREATE INDEX idx_pet_breeds_type_name ON public.pet_breeds (type, name);


-- ============================================================
-- 4. RLS 활성화 (정책은 Step 2-C에서 추가)
-- ============================================================

ALTER TABLE public.pet_breeds ENABLE ROW LEVEL SECURITY;


